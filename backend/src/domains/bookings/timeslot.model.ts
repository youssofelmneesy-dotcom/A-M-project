import { Schema, model } from 'mongoose';

const TimeSlotSchema = new Schema({
    startTime: {
        type: Date,
        required: true
    },
    endTime: {
        type: Date,
        required: true
    },
    isBooked: {
        type: Boolean,
        default: false
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

const TimeSlot = model('TimeSlot', TimeSlotSchema);

export default TimeSlot;